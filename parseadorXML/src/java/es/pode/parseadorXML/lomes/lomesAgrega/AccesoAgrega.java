package es.pode.parseadorXML.lomes.lomesAgrega;

import java.util.ArrayList;

public class AccesoAgrega {

		private String tipoAcceso=null;
		private ArrayList descripcion=null;
		
		
		public String getTipoAcceso() {
			return tipoAcceso;
		}
		public void setTipoAcceso(String tipoAcceso) {
			this.tipoAcceso = tipoAcceso;
		}
		public ArrayList getDescripcion() {
			return descripcion;
		}
		public void setDescripcion(ArrayList descripcion) {
			this.descripcion = descripcion;
		}
		
		
}
